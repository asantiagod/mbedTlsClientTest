const unsigned char psk[] = {
    0x00, 0x01, 0x02, 0x03, 0x04, 0x05, 0x06, 0x07,
    0x08, 0x09, 0x0a, 0x0b, 0x0c, 0x0d, 0x0e, 0x0f
};
const char psk_id[] = "Client_identity";

int main(void)
{
    int aux;
    /* Winsock2 variables */
    WSADATA wsadata;
    SOCKET  sckt;
    SOCKADDR_IN server;
    struct hostent *hp;

    /* mbedTls variables */
    mbedtls_net_context serverContext;
    mbedtls_entropy_context entropy;
    mbedtls_ctr_drbg_context ctr_drbg;
    mbedtls_ssl_context ssl;
    mbedtls_ssl_config conf;
#if defined (MBEDTLS_X509_CRT_PARSE_C)
    mbedtls_x509_crt ca;
#endif

    /*----------- Initialice the mbedTls modules -----------*/
    mbedtls_ctr_drbg_init(&ctr_drbg);
    mbedtls_net_init(&serverContext);
    mbedtls_ssl_init(&ssl);
    mbedtls_ssl_config_init(&conf);
#if defined (MBEDTLS_X509_CRT_PARSE_C)
    mbedtls_x509_crt_init(&ca);
#endif
    mbedtls_entropy_init(&entropy);

    if(mbedtls_ctr_drbg_seed(&ctr_drbg,mbedtls_entropy_func,&entropy,NULL,0))
    {
        puts("CTR-DRBG seed failed\r\n");
        return 1;
    }

    if(mbedtls_ssl_config_defaults(&conf,
                                   MBEDTLS_SSL_IS_CLIENT,
                                   MBEDTLS_SSL_TRANSPORT_STREAM,
                                   MBEDTLS_SSL_PRESET_DEFAULT) != 0)
    {
        puts("Error loading SSL default parameters\r\n");
        return 1;
    }

    mbedtls_ssl_conf_rng(&conf,mbedtls_ctr_drbg_random, &ctr_drbg);

    mbedtls_ssl_conf_psk( &conf, psk, sizeof( psk ),
                (const unsigned char *) psk_id, sizeof( psk_id ) - 1 );

#if defined (MBEDTLS_X509_CRT_PARSE_C)
    if(mbedtls_x509_crt_parse_der(&ca, ca_cert, sizeof(ca_cert)) != 0)
    {
        puts("X509 CRT parse failed");
        return 1;
    }

    mbedtls_ssl_conf_ca_chain(&conf, &ca, NULL);
    mbedtls_ssl_conf_authmode(&conf,MBEDTLS_SSL_VERIFY_OPTIONAL);
#endif

    if(mbedtls_ssl_setup(&ssl,&conf) != 0)
    {
        puts("SSL setup faliled\r\n");
        return 1;
    }

#if defined(MBEDTLS_X509_CRL_PARSE_C)
    if(mbedtls_ssl_set_hostname(&ssl,svrAdd) != 0)
    {
        puts("Host name failed\r\n");
        return 1;
    }
#endif


    /*----------- Initialice Winsock2 library -----------*/
    if(WSAStartup(MAKEWORD(2,2),&wsadata) !=0 )
    {
        printf("Winsock startup failed. Error code: %d\r\n",WSAGetLastError());
        return 2;
    }
    printf("Winsock initialized\r\n");

    /* Initialize a TCP socket */
    sckt = socket(AF_INET,SOCK_STREAM,0);
    if(sckt == INVALID_SOCKET)
    {
        printf("Socket wasn't created. Error code: %d\r\n",WSAGetLastError());
        return 2;
    }
    serverContext.fd = sckt; //set socket descriptor
    printf("Socket created\r\n");

    /* Connect to the server */
    hp = gethostbyname(svrAdd);
    if (hp == NULL)
    {
        printf("gethostbyname() failed\n");
        return 2;
    }

    printf("host ip: %s\r\n",inet_ntoa(*(IN_ADDR*)hp->h_addr_list[0]));
    server.sin_addr = *(IN_ADDR*)hp->h_addr_list[0];
    server.sin_port = htons(svrPort);
    server.sin_family = AF_INET;

    /*----------- Comunication with the server -----------*/
    if(connect(sckt,(SOCKADDR*)&server,sizeof(server)) != 0)
    {
        puts("Connection error\r\n");
        return 2;
    }

    mbedtls_ssl_set_bio(&ssl, &serverContext, mbedtls_net_send, mbedtls_net_recv, NULL);
    aux = mbedtls_ssl_handshake(&ssl);
    if(aux != 0)
    {
        printf("SSL handshake failed. Error code: %d\r\n",aux);
        return 2;
    }

    if(mbedtls_ssl_write(&ssl,(const unsigned char*)testMsg,strlen(testMsg)) < 0)
        puts("Sending error\r\n");
    else
    {
        rxBytes = mbedtls_ssl_read(&ssl,(unsigned char*)rxBuffer,sizeof(rxBuffer)-1u);

        if(rxBytes <= 0)
            puts("Receiving error\r\n");
        else
        {
            rxBuffer[rxBytes] = '\0';
            puts(rxBuffer);
        }
    }
    closesocket(sckt);
    return 0;
}

